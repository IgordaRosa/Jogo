//////////////////////////////////////////////////////////////////////////

#include "Menu.h"

//////////////////////////////////////////////////////////////////////////

Window*& Menu::window = Engine::window;

//////////////////////////////////////////////////////////////////////////
Menu::Menu()
{
    cursor = false;
    apertouEnter = false;
};

//////////////////////////////////////////////////////////////////////////
Menu::~Menu()
{

};

//////////////////////////////////////////////////////////////////////////
void Menu::KeyInteract()
{
    //sair
    if (window->KeyDown(VK_ESCAPE))
        window->Close();

    //cursor
    if (window->KeyDown(VK_RETURN) and !apertouEnter)
    {
        window->HideCursor(!cursor); 
        cursor = !cursor;
        apertouEnter = true;
    }
    if (window->KeyUp(VK_RETURN))
        apertouEnter = false;


};

